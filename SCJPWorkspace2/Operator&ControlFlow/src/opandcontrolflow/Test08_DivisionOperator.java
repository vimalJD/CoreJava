package opandcontrolflow;

public class Test08_DivisionOperator {

	public static void main(String[] args) {

		System.out.println(22 / 7 * 10 * 10);
		System.out.println(22F / 7 * 10 * 10);
		System.out.println(22D / 7 * 10 * 10);
		System.out.println();

		System.out.println(1 / 2);
		System.out.println(1F / 2);

		System.out.println(10 / 3);
		System.out.println(10F / 3);
		System.out.println(10D / 3);
		System.out.println();

		System.out.println(2 / 1);
		System.out.println(0 / 2);
		System.out.println();

//		System.out.println(1 / 0);
//		System.out.println(10 / 0);

		System.out.println(1.0 / 0);
		System.out.println(1.0F / 0);
		System.out.println(-1.0 / 0);
		System.out.println();

//		System.out.println(0 / 0);
		System.out.println(0.0 / 0);
		System.out.println(-0.0 / 0);

		System.out.println(.0 / 0);
		System.out.println(0. / 0);
	}
}
